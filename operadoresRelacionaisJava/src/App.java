public class App {
    public static void main(String[] args) throws Exception {
        
        /*
         == Operador de Igual
         <  Operador de Menor
         >  Operador de Maior
         <= Operador de Menor ou Igual
         >= Operador de Maior ou Igual
         != Operador de Diferente
         */

         int v1 = 20;
         int v2 = 10;
        
        System.out.println(v1 == v2);
        System.out.println(v1 < v2);
        System.out.println(v1 > v2);
        System.out.println(v1 <= v2);
        System.out.println(v1 >= v2);
        System.out.println(v1 != v2);
    }
}
